    static parse(objContent) {
      const vertices = [];
      const normals = [];
      const textures = [];
      const indices = [];
  
      const vertexMatches = objContent.match(/^v\s(.+)$/gm);
      const normalMatches = objContent.match(/^vn\s(.+)$/gm);
      const textureMatches = objContent.match(/^vt\s(.+)$/gm);
      const faceMatches = objContent.match(/^f\s(.+)$/gm);
  
      if (vertexMatches) {
        vertexMatches.forEach(vertex => {
          vertices.push(...vertex.split(' ').slice(1).map(Number));
        });
      }
  
      if (normalMatches) {
        normalMatches.forEach(normal => {
          normals.push(...normal.split(' ').slice(1).map(Number));
        });
      }
  
      if (textureMatches) {
        textureMatches.forEach(texture => {
          textures.push(...texture.split(' ').slice(1).map(Number));
        });
      }
  
      if (faceMatches) {
        faceMatches.forEach(face => {
          const faceVertices = face.split(' ').slice(1);
          faceVertices.forEach(faceVertex => {
            // OBJ indices are 1-based, so we need to subtract 1 for 0-based arrays
            const [vertexIndex] = faceVertex.split('/').map(num => parseInt(num) - 1);
            indices.push(vertexIndex);
          });
        });
      }
  
      return {
        vertices: new Float32Array(vertices),
        normals: new Float32Array(normals),
        textureCoordinates: new Float32Array(textures),
        indices: new Uint16Array(indices)
      };
    }



    







